function addTypes(avar1 , avar2) { //defination of function
    return avar1 + avar2;
}


console.log( addTypes('Priya', 'Reddy') ); //  'PriyaReddy'

console.log( addTypes('Priya', 20) ); //  'Priya20'

console.log( addTypes(300, 'Movies') ); //  '300Movies'

console.log( addTypes('20', 20) ); //  2020